#!/bin/bash
read -p " enter any number out of 10: " number
case $number in
              1)
                 echo "ONE  "
              ;;
              2)
                 echo "TWO "
              ;;
              3)
                 echo "THREE "
              ;;
              4)
                 echo "FOUR  "
              ;;
              5)
                 echo "Five  "
              ;;
              6)
                 echo "SIX "
              ;;
              7)
                 echo "SEVEN "
              ;;
              8)
                 echo "EIGHT"
              ;;
              9)
                 echo "NINE"
              ;;
              10)
                 echo "TEN"
             ;;
esac

